    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
       
        String roll=jTextField6.getText();
        String aos=jTextField7.getText();
        String dbms=jTextField8.getText();
        String ml=jTextField9.getText();
        String ai=jTextField10.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/rms","root","hrishi");

            Statement st=con.createStatement();
              ResultSet rs=st.executeQuery("select * from student where rollno='"+roll+"'");
               if(rs.next())
                       {
                           st.executeUpdate("insert into result(rollno,aos,dbms,ml,ai) values('"+roll+"','"+aos+"','"+dbms+"','"+ml+"','"+ai+"')");
                           JOptionPane.showMessageDialog(null,"Marks saved successfully");
                           setVisible(false);
                           new addResult().setVisible(true);
                       }
               else{
                   JOptionPane.showMessageDialog(null,"This roll number is not registered");
               }
           }
           catch(Exception e)
           {
               JOptionPane.showMessageDialog(null,e.toString());
           }
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

